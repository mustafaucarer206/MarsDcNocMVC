# Klas√∂r√º WatchFolder'a kopyala
function Copy-ToWatchFolder {
    param (
        [string]$folderName,
        [string]$sourcePath
    )
    
    $destinationPath = Get-SafePath -BasePath $watchFolderPath -SubPath $folderName
    
    # Hedef klas√∂r zaten var mƒ±?
    if (Test-Path $destinationPath) {
        Write-DebugLog "$folderName zaten WatchFolder'da mevcut. Atlanƒ±yor." -Level "WARNING"
        return $true
    }
    
    try {
        Write-DebugLog "$folderName WatchFolder'a kopyalanƒ±yor..." -Level "INFO"
        Copy-Item -Path $sourcePath -Destination $destinationPath -Recurse -Force
        Write-DebugLog "$folderName ba≈üarƒ±yla kopyalandƒ±." -Level "SUCCESS"
        
        # Durumu g√ºncelle - IsProcessed = true olarak i≈üaretle
        Update-FolderStatusWithProcessed -folderName $folderName -sizeKB (Get-FolderSize $sourcePath | ForEach-Object { [math]::Round($_ / 1KB, 2) }) -status "MovedToWatchFolder" -isProcessed $true
        
        # Ba≈üarƒ±lƒ± kopyalama sayacƒ±nƒ± artƒ±r
        $global:copiedFolders++
        
        return $true
    }
    catch {
        Write-DebugLog "$folderName kopyalama hatasƒ±: $($_.Exception.Message)" -Level "ERROR"
        Update-FolderStatus -folderName $folderName -sizeKB 0 -status "CopyError"
        
        # Ba≈üarƒ±sƒ±z kopyalama sayacƒ±nƒ± artƒ±r
        $global:failedCopies++
        
        return $false
    }
}

# BackupLogs'da i≈ülenmi≈ü klas√∂rleri al (ba≈üarƒ±lƒ±, hatalƒ±, indiriliyor - hepsini dahil et)
function Get-BackupProcessedFolders {
    try {
        $connection = New-Object System.Data.SqlClient.SqlConnection($global:connectionString)
        $connection.Open()
        
        # BackupLogs tablosunda herhangi bir ≈üekilde i≈ülenmi≈ü klas√∂rleri al
        $query = @"
            SELECT DISTINCT FolderName 
            FROM BackupLogs
"@
        
        Write-DebugLog "BackupLogs'dan i≈ülenmi≈ü klas√∂rler kontrol ediliyor..." -Level "INFO"
        
        $command = $connection.CreateCommand()
        $command.CommandText = $query
        
        $reader = $command.ExecuteReader()
        $processedFolders = @()
        
        while ($reader.Read()) {
            $processedFolders += $reader["FolderName"]
        }
        
        $reader.Close()
        $connection.Close()
        
        Write-DebugLog "BackupLogs'da $($processedFolders.Count) adet i≈ülenmi≈ü klas√∂r bulundu." -Level "INFO"
        return $processedFolders
    }
    catch {
        Write-DebugLog "BackupLogs kontrol hatasƒ±: $_" -Level "ERROR"
        return @()
    }
}

# Ana klas√∂r kontrol fonksiyonu
function Process-DcpOnlineFolders {
    Write-DebugLog "DCP Online klas√∂rler kontrol ediliyor..." -Level "INFO"
    
    # DCP Online klas√∂r√ºn√ºn varlƒ±ƒüƒ±nƒ± kontrol et
    if (-not (Test-Path $dcpOnlinePath)) {
        Write-DebugLog "DCP Online klas√∂r√º bulunamadƒ±: $dcpOnlinePath" -Level "ERROR"
        Start-GracefulShutdown -ExitCode 1 -Reason "DCP Online folder not found"
    }
    
    # BackupLogs'dan i≈ülenmi≈ü klas√∂rleri al (ba≈üarƒ±lƒ±/hatalƒ±/indiriliyor - hepsini dahil et)
    $backupProcessedFolders = Get-BackupProcessedFolders
    
    # T√ºm klas√∂rleri al
    $folders = Get-ChildItem -Path $dcpOnlinePath -Directory
    Write-DebugLog "Toplam $($folders.Count) klas√∂r bulundu" -Level "INFO"
    
    foreach ($folder in $folders) {
        $global:totalFoldersChecked++
        $folderName = $folder.Name
        $folderPath = $folder.FullName
        
        Write-DebugLog "Kontrol ediliyor ($global:totalFoldersChecked/$($folders.Count)): $folderName" -Level "INFO"
        
        # BackupLogs'da zaten i≈ülenmi≈ü mi kontrol et (ba≈üarƒ±lƒ±/hatalƒ±/indiriliyor durumu fark etmez)
        if ($backupProcessedFolders -contains $folderName) {
            Write-DebugLog "‚ö†Ô∏è $folderName zaten BackupLogs'da i≈ülenmi≈ü (ba≈üarƒ±lƒ±/hatalƒ±/indiriliyor). DCP Online Tracker i≈ülemi atlanƒ±yor." -Level "WARNING"
            $global:skippedDueToBackup++
            continue
        }
        
        # Klas√∂r boyutunu hesapla
        $folderSize = Get-FolderSize -folderPath $folderPath
        $folderSizeKB = [math]::Round($folderSize / 1KB, 2)
        Write-DebugLog "$folderName boyutu: $folderSizeKB KB" -Level "INFO"
        
        # Klas√∂r√ºn durumunu al
        $status = Get-FolderStatus -folderName $folderName -currentSizeKB $folderSizeKB
        
        if (-not $status.Exists) {
            # 1. ƒ∞LK KONTROL: Yeni klas√∂r
            Write-DebugLog "$folderName yeni klas√∂r. Veritabanƒ±na ekleniyor (Status: New, IsProcessed: false)." -Level "INFO"
            Update-FolderStatus -folderName $folderName -sizeKB $folderSizeKB -status "New"
        }
        elseif ($status.Status -eq "New" -and $status.IsStable -and $status.MinutesSinceLastChange -ge $stableWaitMinutes) {
            # 2. ƒ∞Kƒ∞NCƒ∞ KONTROL: 5+ dakika boyut deƒüi≈ümemi≈ü -> Stable yap
            Write-DebugLog "$folderName stabil hale geldi ($([math]::Round($status.MinutesSinceLastChange, 1)) dakika boyut deƒüi≈ümemi≈ü). Status: Stable yapƒ±lƒ±yor." -Level "SUCCESS"
            Update-FolderStatus -folderName $folderName -sizeKB $folderSizeKB -status "Stable"
        }
        elseif ($status.Status -eq "Stable" -and $status.IsStable) {
            # 3. √ú√á√úNC√ú KONTROL: Stable durumda -> WatchFolder'a kopyala
            $global:stableFolders++
            Write-DebugLog "$folderName stabil durumda. WatchFolder'a kopyalanƒ±yor." -Level "SUCCESS"
            
            if (Copy-ToWatchFolder -folderName $folderName -sourcePath $folderPath) {
                Write-DebugLog "$folderName ba≈üarƒ±yla i≈ülendi (Status: MovedToWatchFolder, IsProcessed: true)." -Level "SUCCESS"
            }
        }
        elseif (-not $status.IsStable) {
            # BOYUT DEƒûƒ∞≈ûMƒ∞≈û: Boyut deƒüi≈ütiƒüinde tekrar New yap
            Write-DebugLog "$folderName boyutu deƒüi≈ümi≈ü. √ñnceki: $($status.LastSize) KB, Yeni: $folderSizeKB KB. Status: New'e d√∂nd√ºr√ºl√ºyor." -Level "WARNING"
            Update-FolderStatus -folderName $folderName -sizeKB $folderSizeKB -status "New"
        }
        else {
            # HEN√úZ BEKLEMEDE: Yeterince zaman ge√ßmemi≈ü
            $remainingMinutes = $stableWaitMinutes - $status.MinutesSinceLastChange
            Write-DebugLog "$folderName daha $([math]::Round($remainingMinutes, 1)) dakika beklemeli (Status: $($status.Status))." -Level "INFO"
        }
    }
    
    Write-DebugLog "Klas√∂r kontrol√º tamamlandƒ±. ƒ∞≈ülem √∂zeti:" -Level "INFO"
    Write-DebugLog "  - Toplam kontrol edilen: $global:totalFoldersChecked" -Level "INFO"
    Write-DebugLog "  - Yeni bulunan: $global:newFoldersFound" -Level "INFO"
    Write-DebugLog "  - Stabil olan: $global:stableFolders" -Level "INFO"
    Write-DebugLog "  - Ba≈üarƒ±yla kopyalanan: $global:copiedFolders" -Level "SUCCESS"
    Write-DebugLog "  - Kopyalama ba≈üarƒ±sƒ±z: $global:failedCopies" -Level $(if ($global:failedCopies -gt 0) { "ERROR" } else { "INFO" })
}

# Eski klas√∂rleri temizle
function Clean-OldFolders {
    Write-DebugLog "Eski klas√∂rler temizleniyor ($oldFolderHours saatten eski)..." -Level "INFO"
    
    if (-not (Test-Path $dcpOnlinePath)) {
        return
    }
    
    $cutoffTime = (Get-Date).AddHours(-$oldFolderHours)
    $oldFolders = Get-ChildItem -Path $dcpOnlinePath -Directory | Where-Object { $_.LastWriteTime -lt $cutoffTime }
    
    if ($oldFolders.Count -eq 0) {
        Write-DebugLog "Silinecek eski klas√∂r bulunamadƒ±." -Level "INFO"
        return
    }
    
    Write-DebugLog "$($oldFolders.Count) eski klas√∂r bulundu. Siliniyor..." -Level "WARNING"
    
    foreach ($oldFolder in $oldFolders) {
        try {
            Write-DebugLog "$($oldFolder.Name) klas√∂r√º siliniyor (Son deƒüi≈üiklik: $($oldFolder.LastWriteTime))..." -Level "WARNING"
            
            # Veritabanƒ±ndan sil - Devre dƒ±≈üƒ± bƒ±rakƒ±ldƒ± (kayƒ±tlar korunacak)
            # $connection = New-Object System.Data.SqlClient.SqlConnection($global:connectionString)
            # $connection.Open()
            # $deleteQuery = "DELETE FROM DcpOnlineFolderTracking WHERE FolderName = @folderName"
            # $deleteCommand = $connection.CreateCommand()
            # $deleteCommand.CommandText = $deleteQuery
            # $deleteCommand.Parameters.AddWithValue("@folderName", $oldFolder.Name)
            # $deleteCommand.ExecuteNonQuery()
            # $connection.Close()
            Write-DebugLog "$($oldFolder.Name) fiziksel olarak siliniyor ancak DcpOnlineFolderTracking kaydƒ± korunuyor" -Level "INFO"
            
            # Fiziksel olarak sil
            Remove-Item -Path $oldFolder.FullName -Recurse -Force
            
            # Silinen klas√∂r sayacƒ±nƒ± artƒ±r
            $global:oldFoldersDeleted++
            
            Write-DebugLog "$($oldFolder.Name) ba≈üarƒ±yla silindi." -Level "SUCCESS"
        }
        catch {
            Write-DebugLog "$($oldFolder.Name) silme hatasƒ±: $($_.Exception.Message)" -Level "ERROR"
        }
    }
    
    Write-DebugLog "Eski klas√∂r temizliƒüi tamamlandƒ±. Silinen klas√∂r sayƒ±sƒ±: $global:oldFoldersDeleted" -Level "INFO"
}

