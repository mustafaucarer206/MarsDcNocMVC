function Start-GracefulShutdown {
    param (
        [int]$ExitCode = 0,
        [string]$Reason = "Normal completion"
    )
    
    $global:scriptStatus = if ($ExitCode -eq 0) { "Completed Successfully" } else { "Completed with Errors" }
